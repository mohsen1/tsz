//! Node modules and bare specifier resolution.
//!
//! Handles resolution of bare specifiers (npm packages) by walking up
//! `node_modules` directories, resolving package entry points, and
//! Classic (non-node_modules) resolution.

use super::{
    ImportingModuleKind, ModuleExtension, ModuleResolver, ResolutionFailure, ResolvedModule,
    self_reference::SelfReferenceResultV2,
};
use crate::config::ModuleResolutionKind;
use crate::module_resolver_helpers::*;
use crate::span::Span;
use std::path::{Path, PathBuf};

impl ModuleResolver {
    fn declaration_file_declares_ambient_module(&self, path: &Path, specifier: &str) -> bool {
        if !matches!(
            ModuleExtension::from_path(path),
            ModuleExtension::Dts | ModuleExtension::DmTs | ModuleExtension::DCts
        ) {
            return false;
        }

        let Ok(text) = std::fs::read_to_string(path) else {
            return false;
        };

        text.contains(&format!("declare module \"{specifier}\""))
            || text.contains(&format!("declare module '{specifier}'"))
    }

    fn try_ambient_package_entry_for_subpath(
        &self,
        package_dir: &Path,
        package_json: &PackageJson,
        original_specifier: &str,
    ) -> Option<PathBuf> {
        let mut candidates = Vec::new();

        if let Some(types_versions) = &package_json.types_versions
            && let Some(resolved) =
                self.resolve_types_versions(package_dir, "index", types_versions)
        {
            candidates.push(resolved);
        }

        if let Some(types) = package_json
            .types
            .clone()
            .or_else(|| package_json.typings.clone())
        {
            let types_path = package_dir.join(&types);
            if let Some(resolved) = self.try_types_entry(&types_path)
                && !candidates.contains(&resolved)
            {
                candidates.push(resolved);
            }
        }

        candidates.into_iter().find(|candidate| {
            self.declaration_file_declares_ambient_module(candidate, original_specifier)
        })
    }

    /// Resolve a non-relative module specifier using Classic resolution.
    ///
    /// TypeScript's Classic algorithm walks up the directory tree from the containing
    /// file's directory, probing for `<specifier>.ts`, `<specifier>.tsx`,
    /// `<specifier>.d.ts` at each level. It does NOT consult `node_modules`.
    ///
    /// Example: importing `"foo"` from `/a/b/c/app.ts` will try:
    ///   /a/b/c/foo.ts, /a/b/c/foo.tsx, /a/b/c/foo.d.ts, ...
    ///   /a/b/foo.ts, /a/b/foo.tsx, /a/b/foo.d.ts, ...
    ///   /a/foo.ts, /a/foo.tsx, /a/foo.d.ts, ...
    ///   /foo.ts, /foo.tsx, /foo.d.ts, ...
    pub(super) fn resolve_classic_non_relative(
        &self,
        specifier: &str,
        containing_dir: &Path,
        containing_file: &str,
        specifier_span: Span,
    ) -> Result<ResolvedModule, ResolutionFailure> {
        let (package_name, subpath) = parse_package_specifier(specifier);
        let conditions = self.get_export_conditions(ImportingModuleKind::CommonJs);

        let mut current = containing_dir.to_path_buf();
        loop {
            let candidate = current.join(specifier);
            if let Some(resolved) = self.try_file_or_directory(&candidate) {
                return Ok(ResolvedModule {
                    resolved_path: resolved.clone(),
                    resolved_using_ts_extension: false,
                    is_external: false,
                    package_name: None,
                    original_specifier: specifier.to_string(),
                    extension: ModuleExtension::from_path(&resolved),
                });
            }

            // Also check @types packages in node_modules (TypeScript classic resolution
            // still resolves @types packages for bare specifiers)
            if !package_name.starts_with("@types/") {
                let types_package = types_package_name(&package_name);
                let types_dir = current.join("node_modules").join(&types_package);
                if types_dir.is_dir()
                    && let Ok(resolved) = self.resolve_package(
                        &types_dir,
                        subpath.as_deref(),
                        specifier,
                        containing_file,
                        specifier_span,
                        &conditions,
                    )
                {
                    return Ok(resolved);
                }
            }

            // Check type_roots for the package
            for type_root in &self.type_roots {
                let types_package = if !package_name.starts_with("@types/") {
                    type_root.join(types_package_name(&package_name))
                } else {
                    type_root.join(&package_name)
                };
                if types_package.is_dir()
                    && let Ok(resolved) = self.resolve_package(
                        &types_package,
                        subpath.as_deref(),
                        specifier,
                        containing_file,
                        specifier_span,
                        &conditions,
                    )
                {
                    return Ok(resolved);
                }
            }

            // Move to parent directory
            match current.parent() {
                Some(parent) if parent != current => current = parent.to_path_buf(),
                _ => break,
            }
        }

        // Module not found - emit TS2307 (standard "Cannot find module" error).
        // Classic resolution walks up the directory tree but doesn't use node_modules.
        Err(ResolutionFailure::NotFound {
            specifier: specifier.to_string(),
            containing_file: containing_file.to_string(),
            span: specifier_span,
        })
    }

    /// Resolve a bare specifier (npm package)
    pub(super) fn resolve_bare_specifier(
        &self,
        specifier: &str,
        containing_dir: &Path,
        containing_file: &str,
        specifier_span: Span,
        importing_module_kind: ImportingModuleKind,
    ) -> Result<ResolvedModule, ResolutionFailure> {
        // Parse package name and subpath
        let (package_name, subpath) = parse_package_specifier(specifier);
        let conditions = self.get_export_conditions(importing_module_kind);

        // First, try self-reference: check if we're inside a package that matches the specifier
        match self.try_self_reference_v2(
            &package_name,
            subpath.as_deref(),
            specifier,
            containing_dir,
            containing_file,
            specifier_span,
            &conditions,
        ) {
            SelfReferenceResultV2::Resolved(resolved) => return Ok(resolved),
            SelfReferenceResultV2::ExportsFailed { .. } => {
                // Self-reference detected but exports didn't resolve to an existing file.
                // Fall through to node_modules resolution - the module may resolve there.
            }
            SelfReferenceResultV2::NotSelfReference => {
                // Not a self-reference, continue to node_modules resolution
            }
        }

        // Walk up directory tree looking for node_modules
        let mut current = containing_dir.to_path_buf();
        loop {
            let node_modules = current.join("node_modules");

            if node_modules.is_dir() {
                let package_dir = node_modules.join(&package_name);

                if package_dir.is_dir() {
                    match self.resolve_package(
                        &package_dir,
                        subpath.as_deref(),
                        specifier,
                        containing_file,
                        specifier_span,
                        &conditions,
                    ) {
                        Ok(resolved) => return Ok(resolved),
                        Err(e @ ResolutionFailure::NotFound { .. }) => {
                            // In Node16/NodeNext/Bundler, exports field is authoritative —
                            // don't continue searching parent node_modules.
                            if self.should_stop_on_exports_failure(
                                &package_dir,
                                subpath.as_deref(),
                                &conditions,
                            ) {
                                return Err(e);
                            }
                        }
                        Err(_) => {
                            // Continue searching in parent directories
                        }
                    }
                } else if matches!(self.resolution_kind, ModuleResolutionKind::Bundler)
                    && subpath.is_none()
                {
                    // In bundler mode, package specifiers may resolve directly to
                    // files like `node_modules/foo.d.ts`.
                    if let Some(resolved) = self.try_file_or_directory(&package_dir) {
                        return Ok(ResolvedModule {
                            resolved_path: resolved.clone(),
                            resolved_using_ts_extension: false,
                            is_external: true,
                            package_name: Some(package_name),
                            original_specifier: specifier.to_string(),
                            extension: ModuleExtension::from_path(&resolved),
                        });
                    }
                }
            }

            if !package_name.starts_with("@types/") {
                let types_package = types_package_name(&package_name);
                let types_dir = node_modules.join(&types_package);
                if types_dir.is_dir()
                    && let Ok(resolved) = self.resolve_package(
                        &types_dir,
                        subpath.as_deref(),
                        specifier,
                        containing_file,
                        specifier_span,
                        &conditions,
                    )
                {
                    return Ok(resolved);
                }
            }

            // Move to parent directory
            match current.parent() {
                Some(parent) if parent != current => current = parent.to_path_buf(),
                _ => break,
            }
        }

        // Try type roots (for @types packages)
        for type_root in &self.type_roots {
            let types_package = type_root.join(types_package_name(&package_name));
            if types_package.is_dir()
                && let Ok(resolved) = self.resolve_package(
                    &types_package,
                    subpath.as_deref(),
                    specifier,
                    containing_file,
                    specifier_span,
                    &conditions,
                )
            {
                return Ok(resolved);
            }
        }

        Err(ResolutionFailure::NotFound {
            specifier: specifier.to_string(),
            containing_file: containing_file.to_string(),
            span: specifier_span,
        })
    }

    pub(super) fn should_stop_on_exports_failure(
        &self,
        package_dir: &Path,
        subpath: Option<&str>,
        conditions: &[String],
    ) -> bool {
        if !matches!(
            self.resolution_kind,
            ModuleResolutionKind::Bundler
                | ModuleResolutionKind::Node16
                | ModuleResolutionKind::NodeNext
        ) {
            return false;
        }
        if !self.resolve_package_json_exports {
            return false;
        }

        let package_json_path = package_dir.join("package.json");
        if !package_json_path.is_file() {
            return false;
        }

        let package_json = match self.read_package_json(&package_json_path) {
            Ok(package_json) => package_json,
            Err(_) => return false,
        };

        let Some(exports) = package_json.exports else {
            return false;
        };

        let subpath_key = match subpath {
            Some(subpath) => format!("./{subpath}"),
            None => ".".to_string(),
        };

        self.resolve_package_exports_with_conditions(
            package_dir,
            &exports,
            &subpath_key,
            conditions,
        )
        .is_none()
    }

    /// Resolve within a package directory
    pub(super) fn resolve_package(
        &self,
        package_dir: &Path,
        subpath: Option<&str>,
        original_specifier: &str,
        containing_file: &str,
        specifier_span: Span,
        conditions: &[String],
    ) -> Result<ResolvedModule, ResolutionFailure> {
        // Read package.json
        let package_json_path = package_dir.join("package.json");
        let package_json = if package_json_path.exists() {
            self.read_package_json(&package_json_path).map_err(|msg| {
                ResolutionFailure::PackageJsonError {
                    message: msg,
                    containing_file: containing_file.to_string(),
                    span: specifier_span,
                }
            })?
        } else {
            PackageJson::default()
        };

        // If there's a subpath, resolve it directly
        if let Some(subpath) = subpath {
            let subpath_key = format!("./{subpath}");

            // Try exports field first (Node16+)
            if self.resolve_package_json_exports
                && let Some(exports) = &package_json.exports
            {
                if let Some(resolved) = self.resolve_package_exports_with_conditions(
                    package_dir,
                    exports,
                    &subpath_key,
                    conditions,
                ) {
                    return Ok(ResolvedModule {
                        resolved_path: resolved.clone(),
                        resolved_using_ts_extension: false,
                        is_external: true,
                        package_name: Some(package_json.name.clone().unwrap_or_default()),
                        original_specifier: original_specifier.to_string(),
                        extension: ModuleExtension::from_path(&resolved),
                    });
                }
                // In Node16/NodeNext/Bundler, exports field is authoritative for subpaths.
                // Return NotFound (TS2307) — the driver handles Classic→TS2792 conversion.
                if matches!(
                    self.resolution_kind,
                    ModuleResolutionKind::Node16
                        | ModuleResolutionKind::NodeNext
                        | ModuleResolutionKind::Bundler
                ) {
                    return Err(ResolutionFailure::NotFound {
                        specifier: original_specifier.to_string(),
                        containing_file: containing_file.to_string(),
                        span: specifier_span,
                    });
                }
            }

            // Try typesVersions field
            if let Some(types_versions) = &package_json.types_versions
                && let Some(resolved) =
                    self.resolve_types_versions(package_dir, subpath, types_versions)
            {
                return Ok(ResolvedModule {
                    resolved_path: resolved.clone(),
                    resolved_using_ts_extension: false,
                    is_external: true,
                    package_name: Some(package_json.name.clone().unwrap_or_default()),
                    original_specifier: original_specifier.to_string(),
                    extension: ModuleExtension::from_path(&resolved),
                });
            }

            // Fall back to direct file resolution.
            // In Node16/NodeNext with ESM packages (no exports field), Node.js does
            // not perform directory index resolution. Only try file-based resolution
            // to match tsc behavior.
            let file_path = package_dir.join(subpath);
            let is_esm_package = matches!(
                self.resolution_kind,
                ModuleResolutionKind::Node16 | ModuleResolutionKind::NodeNext
            ) && package_json
                .package_type
                .as_deref()
                .is_some_and(|t| t == "module");
            let resolved = if is_esm_package {
                self.try_file_no_index(&file_path)
            } else {
                self.try_file_or_directory(&file_path)
            };
            if let Some(resolved) = resolved {
                return Ok(ResolvedModule {
                    resolved_path: resolved.clone(),
                    resolved_using_ts_extension: false,
                    is_external: true,
                    package_name: Some(package_json.name.unwrap_or_default()),
                    original_specifier: original_specifier.to_string(),
                    extension: ModuleExtension::from_path(&resolved),
                });
            }

            if let Some(resolved) = self.try_ambient_package_entry_for_subpath(
                package_dir,
                &package_json,
                original_specifier,
            ) {
                return Ok(ResolvedModule {
                    resolved_path: resolved.clone(),
                    resolved_using_ts_extension: false,
                    is_external: true,
                    package_name: Some(package_json.name.unwrap_or_default()),
                    original_specifier: original_specifier.to_string(),
                    extension: ModuleExtension::from_path(&resolved),
                });
            }

            return Err(ResolutionFailure::NotFound {
                specifier: original_specifier.to_string(),
                containing_file: containing_file.to_string(),
                span: specifier_span,
            });
        }

        // No subpath - resolve package entry point

        // Try exports "." field first (Node16+)
        if self.resolve_package_json_exports
            && let Some(exports) = &package_json.exports
        {
            if let Some(resolved) =
                self.resolve_package_exports_with_conditions(package_dir, exports, ".", conditions)
            {
                return Ok(ResolvedModule {
                    resolved_path: resolved.clone(),
                    resolved_using_ts_extension: false,
                    is_external: true,
                    package_name: Some(package_json.name.clone().unwrap_or_default()),
                    original_specifier: original_specifier.to_string(),
                    extension: ModuleExtension::from_path(&resolved),
                });
            }
            // In Node16/NodeNext/Bundler, exports field is authoritative.
            // Return NotFound (TS2307) — the driver handles Classic→TS2792 conversion.
            if matches!(
                self.resolution_kind,
                ModuleResolutionKind::Node16
                    | ModuleResolutionKind::NodeNext
                    | ModuleResolutionKind::Bundler
            ) {
                return Err(ResolutionFailure::NotFound {
                    specifier: original_specifier.to_string(),
                    containing_file: containing_file.to_string(),
                    span: specifier_span,
                });
            }
        }

        // Try typesVersions field for index
        if let Some(types_versions) = &package_json.types_versions
            && let Some(resolved) =
                self.resolve_types_versions(package_dir, "index", types_versions)
        {
            return Ok(ResolvedModule {
                resolved_path: resolved.clone(),
                resolved_using_ts_extension: false,
                is_external: true,
                package_name: Some(package_json.name.clone().unwrap_or_default()),
                original_specifier: original_specifier.to_string(),
                extension: ModuleExtension::from_path(&resolved),
            });
        }

        // Try types/typings field. These entries are declaration-oriented and
        // must not resolve to runtime JS files directly.
        if let Some(types) = package_json
            .types
            .clone()
            .or_else(|| package_json.typings.clone())
        {
            let types_path = package_dir.join(&types);
            if let Some(resolved) = self.try_types_entry(&types_path) {
                return Ok(ResolvedModule {
                    resolved_path: resolved.clone(),
                    resolved_using_ts_extension: false,
                    is_external: true,
                    package_name: Some(package_json.name.unwrap_or_default()),
                    original_specifier: original_specifier.to_string(),
                    extension: ModuleExtension::from_path(&resolved),
                });
            }
        }

        // Try main field
        if let Some(main) = &package_json.main {
            let main_path = package_dir.join(main);
            if self.allow_arbitrary_extensions
                && let Some(resolved) = resolve_explicit_unknown_extension(&main_path)
            {
                return Ok(ResolvedModule {
                    resolved_path: resolved.clone(),
                    resolved_using_ts_extension: false,
                    is_external: true,
                    package_name: Some(package_json.name.clone().unwrap_or_default()),
                    original_specifier: original_specifier.to_string(),
                    extension: ModuleExtension::from_path(&resolved),
                });
            }
            if let Some(declaration) = declaration_substitution_for_main(&main_path)
                && declaration.is_file()
            {
                return Ok(ResolvedModule {
                    resolved_path: declaration.clone(),
                    resolved_using_ts_extension: false,
                    is_external: true,
                    package_name: Some(package_json.name.clone().unwrap_or_default()),
                    original_specifier: original_specifier.to_string(),
                    extension: ModuleExtension::from_path(&declaration),
                });
            }
            // Try the main path as a file (with extension probing)
            if let Some(resolved) = self.try_file(&main_path) {
                return Ok(ResolvedModule {
                    resolved_path: resolved.clone(),
                    resolved_using_ts_extension: false,
                    is_external: true,
                    package_name: Some(package_json.name.clone().unwrap_or_default()),
                    original_specifier: original_specifier.to_string(),
                    extension: ModuleExtension::from_path(&resolved),
                });
            }
            // For main field targets that are directories, only try index files.
            // Do NOT read nested package.json — main field resolution is non-recursive.
            if main_path.is_dir() {
                let index = main_path.join("index");
                if let Some(resolved) = self.try_file(&index) {
                    return Ok(ResolvedModule {
                        resolved_path: resolved.clone(),
                        resolved_using_ts_extension: false,
                        is_external: true,
                        package_name: Some(package_json.name.clone().unwrap_or_default()),
                        original_specifier: original_specifier.to_string(),
                        extension: ModuleExtension::from_path(&resolved),
                    });
                }
            }
        }

        // Try index.ts/index.js as a fallback, matching Node.js package behavior when
        // package.json is absent in node_modules.
        let index = package_dir.join("index");
        if let Some(resolved) = self.try_file(&index) {
            return Ok(ResolvedModule {
                resolved_path: resolved.clone(),
                resolved_using_ts_extension: false,
                is_external: true,
                package_name: Some(package_json.name.unwrap_or_default()),
                original_specifier: original_specifier.to_string(),
                extension: ModuleExtension::from_path(&resolved),
            });
        }

        Err(ResolutionFailure::PackageJsonError {
            message: format!(
                "Could not find entry point for package at {}",
                package_dir.display()
            ),
            containing_file: containing_file.to_string(),
            span: specifier_span,
        })
    }
}
