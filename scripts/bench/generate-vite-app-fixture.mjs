write("src/view.ts", `import { summarizeSeries, type DataPoint } from "./metrics";

export interface DashboardInput {
  title: string;
  logos: readonly string[];
  heroUrl: string;
}

export interface DashboardModel extends DashboardInput {
  points: DataPoint[];
  summary: ReturnType<typeof summarizeSeries>;
}

export function createDashboardModel(input: DashboardInput): DashboardModel {
  const points = Array.from({ length: 36 }, (_, index) => ({
    label: \`sample-\${index + 1}\`,
    value: Math.round((Math.sin(index / 3) + 2) * 100 + index * 7),
  }));
  return {
    ...input,
    points,
    summary: summarizeSeries(points),
  };
}

export function renderDashboard(model: DashboardModel): string {
  const logos = model.logos
    .map((src) => \`<img src="\${src}" width="48" height="48" alt="" />\`)
    .join("");
  return [
    '<main class="dashboard">',
    \`<h1>\${model.title}</h1>\`,
    \`<img src="\${model.heroUrl}" width="320" height="180" alt="" />\`,
    \`<div>\${logos}</div>\`,
    \`<p>mean: \${model.summary.mean.toFixed(2)}</p>\`,
    \`<p>p95: \${model.summary.p95.toFixed(2)}</p>\`,
    "</main>",
  ].join("");
}
`);

write("src/metrics.ts", `export interface DataPoint {
  label: string;
  value: number;
}

export interface SeriesSummary {
  min: number;
  max: number;
  mean: number;
  p95: number;
}

export function summarizeSeries(points: readonly DataPoint[]): SeriesSummary {
  const values = points.map((point) => point.value).sort((left, right) => left - right);
  const total = values.reduce((sum, value) => sum + value, 0);
  return {
    min: values[0] ?? 0,
    max: values[values.length - 1] ?? 0,
    mean: values.length === 0 ? 0 : total / values.length,
    p95: percentile(values, 0.95),
  };
}

function percentile(sortedValues: readonly number[], rank: number): number {
  if (sortedValues.length === 0) {
    return 0;
  }
  const index = Math.min(sortedValues.length - 1, Math.ceil(sortedValues.length * rank) - 1);
  return sortedValues[index] ?? 0;
}
`);

