function packageDir(packageName) {
  const parts = packageName.startsWith("@") ? packageName.split("/") : [packageName];
  return path.join(outputDir, "node_modules", ...parts);
}

function patchPackageTypes(packageName, declaration, options = {}) {
  const dir = packageDir(packageName);
  const packageJsonPath = path.join(dir, "package.json");
  const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, "utf8"));
  const typesFile = options.typesFile || "tsz-benchmark.d.ts";
  const jsFile = typesFile.replace(/\.d\.ts$/, ".js");
  fs.writeFileSync(path.join(dir, typesFile), declaration, "utf8");
  fs.writeFileSync(path.join(dir, jsFile), "export {};\n", "utf8");

  packageJson.types = `./${typesFile}`;
  packageJson.typings = `./${typesFile}`;
  packageJson.main = `./${jsFile}`;
  packageJson.exports = {
    ".": {
      types: `./${typesFile}`,
      default: `./${jsFile}`,
    },
    ...(options.exports || {}),
  };
  fs.writeFileSync(packageJsonPath, `${JSON.stringify(packageJson, null, 2)}\n`, "utf8");
}

